//Multiplication of no. of choice 

// for loop
let n = 8;
for(let i=1;i<=10;i++){
  console.log(`${n} * ${i} = ${n*i}`);
}

// //while loop
let j = 1; //initilization
while (j <= 10) {
  console.log(`${n} * ${j} = ${n*j}`);
  j++; //update
}


//do while loop
let k = 1; //initilize
do {
  console.log(`${n} * ${k} = ${n*k}`);
  k++; //update
}while(k <= 10);